    private int color_code, red_comp, green_comp, blue_comp;

    public MCColor(int code) {
        color_code = code;
        setRGB(R(code), G(code), B(code));
    }

    public MCColor(int red, int green, int blue) {
        color_code = colorInteger(red, green, blue);
        setRGB(red, green, blue);
    }

    public MCColor(float red, float green, float blue) {
        color_code = colorInteger(red, green, blue);
        setRGB((int) (red * 255), (int) (green * 255), (int) (blue * 255));
    }

    public void setRGB(int r, int g, int b) {
        red_comp = r;
        green_comp = g;
        blue_comp = b;

        color_code = colorInteger(red_comp, green_comp, blue_comp);
    }

    public int value() {
        return color_code;
    }

    public int getRed() {
        return red_comp;
    }

    public int getGreen() {
        return green_comp;
    }

    public int getBlue() {
        return blue_comp;
    }

    public static int colorInteger(Float[] c) {
        return colorInteger(c[0], c[1], c[2]);
    }

    public static int colorInteger(float[] c) {
        return colorInteger(c[0], c[1], c[2]);
    }

    public static int colorInteger(float r, float g, float b) {
        return colorInteger((int) (r * 255), (int) (g * 255), (int) (b * 255));
    }

    public static int colorInteger(int r, int g, int b) {
        return (r << 16) | (g << 8) | (b);
    }

    public static Integer[] colorIntComponents(int c) {
        return new Integer[]{R(c), G(c), B(c)};
    }

    public static float[] colorComponents(int c) {
        return new float[]{r(c), g(c), b(c)};
    }

    public static float r(int c) {
        return (float) (R(c)) / 255.0F;
    }

    public static float g(int c) {
        return (float) (G(c)) / 255.0F;
    }

    public static float b(int c) {
        return (float) (B(c)) / 255.0F;
    }

    public static int R(int c) {
        return c >> 16 & 255;
    }

    public static int G(int c) {
        return c >> 8 & 255;
    }

    public static int B(int c) {
        return c & 255;
    }

    public static float[] newRGBarray(float rgb) {
        return newRGBarray(rgb, rgb, rgb);
    }

    public static float[] newRGBarray(float r, float g, float b) {
        return new float[]{r, g, b};
    }

    public static float[] multRGBarray(float seed, float[] mult) {
        float[] result = new float[mult.length];
        for (int i = 0; i < result.length; i++) {
            result[i] = mult[i] * seed;
        }

        return result;
    }
